# 1️⃣3️⃣ BankAccount System
class BankAccount:
    bank_name = "AlFalah Bank"

    def __init__(self, owner, balance):
        self.owner = owner
        self.__balance = balance   # private attribute (encapsulation)

    def get_balance(self):
        return self.__balance

    def __str__(self):
        return f"Account Owner: {self.owner}, Balance: {self.__balance}"

    def __add__(self, other):
        total = self.__balance + other.__balance
        return BankAccount("Combined", total)

    @classmethod
    def set_bank_name(cls, name):
        cls.bank_name = name
        print(f"Bank name set to: {cls.bank_name}")

    @staticmethod
    def validate_ifsc(ifsc):
        # Simple rule: 11 characters, starts with 4 letters
        if len(ifsc) == 11 and ifsc[:4].isalpha():
            print(f"IFSC {ifsc} is Valid")
        else:
            print(f"IFSC {ifsc} is Invalid")


class SavingsAccount(BankAccount):
    def __init__(self, owner, balance, interest_rate):
        super().__init__(owner, balance)
        self.interest_rate = interest_rate

    def __str__(self):
        return f"[Savings] {super().__str__()}, Interest Rate: {self.interest_rate}%"


class CurrentAccount(BankAccount):
    def __init__(self, owner, balance, overdraft_limit):
        super().__init__(owner, balance)
        self.overdraft_limit = overdraft_limit

    def __str__(self):
        return f"[Current] {super().__str__()}, Overdraft Limit: {self.overdraft_limit}"


print("\n--- BankAccount System ---")
sa = SavingsAccount("Ali", 10000, 5)
ca = CurrentAccount("Sara", 20000, 5000)

print(sa)
print(ca)

combined = sa + ca
print("Combined Account Balance:", combined.get_balance())

