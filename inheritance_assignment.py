# 4️⃣ Method Overriding
class Shape:
    def area(self):
        print("Area of shape")

class Rectangle(Shape):
    def __init__(self, length, width):
        self.length = length
        self.width = width

    def area(self):
        print(f"Rectangle Area: {self.length * self.width}")

class Circle(Shape):
    def __init__(self, radius):
        self.radius = radius

    def area(self):
        print(f"Circle Area: {3.14 * self.radius * self.radius:.2f}")

print("\n--- Method Overriding ---")
r = Rectangle(5, 3)
r.area()

ci = Circle(7)
ci.area()


# 5️⃣ Operator Overloading
class Number:
    def __init__(self, value):
        self.value = value

    def __add__(self, other):
        # self  = LHS object (n1)
        # other = RHS object (n2)
        return Number(self.value + other.value)

    def display(self):
        print(f"Value: {self.value}")

print("\n--- Operator Overloading ---")
n1 = Number(10)
n2 = Number(20)
n3 = n1 + n2   # calls n1.__add__(n2)
n3.display()

# Explanation:
# self  → refers to the LEFT side object  (n1)
# other → refers to the RIGHT side object (n2) — also called RHS object


