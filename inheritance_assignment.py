# 8️⃣ Class Method
class Company:
    company_name = "TechCorp"

    @classmethod
    def change_company_name(cls, new_name):
        cls.company_name = new_name
        print(f"Company name changed to: {cls.company_name}")

print("\n--- Class Method ---")
